package com.mmuhamadamirzaidi.sellynapp.Model;

public class Holder {

    private String manufacturerAddress1, manufacturerAddress2, manufacturerAddress3, manufacturerDistrict, manufacturerImage, manufacturerName, manufacturerPhoneNo, manufacturerPostcode, manufacturerState;

    public Holder() {
    }

    public Holder(String manufacturerAddress1, String manufacturerAddress2, String manufacturerAddress3, String manufacturerDistrict, String manufacturerImage, String manufacturerName, String manufacturerPhoneNo, String manufacturerPostcode, String manufacturerState) {
        this.manufacturerAddress1 = manufacturerAddress1;
        this.manufacturerAddress2 = manufacturerAddress2;
        this.manufacturerAddress3 = manufacturerAddress3;
        this.manufacturerDistrict = manufacturerDistrict;
        this.manufacturerImage = manufacturerImage;
        this.manufacturerName = manufacturerName;
        this.manufacturerPhoneNo = manufacturerPhoneNo;
        this.manufacturerPostcode = manufacturerPostcode;
        this.manufacturerState = manufacturerState;
    }

    public String getManufacturerAddress1() {
        return manufacturerAddress1;
    }

    public void setManufacturerAddress1(String manufacturerAddress1) {
        this.manufacturerAddress1 = manufacturerAddress1;
    }

    public String getManufacturerAddress2() {
        return manufacturerAddress2;
    }

    public void setManufacturerAddress2(String manufacturerAddress2) {
        this.manufacturerAddress2 = manufacturerAddress2;
    }

    public String getManufacturerAddress3() {
        return manufacturerAddress3;
    }

    public void setManufacturerAddress3(String manufacturerAddress3) {
        this.manufacturerAddress3 = manufacturerAddress3;
    }

    public String getManufacturerDistrict() {
        return manufacturerDistrict;
    }

    public void setManufacturerDistrict(String manufacturerDistrict) {
        this.manufacturerDistrict = manufacturerDistrict;
    }

    public String getManufacturerImage() {
        return manufacturerImage;
    }

    public void setManufacturerImage(String manufacturerImage) {
        this.manufacturerImage = manufacturerImage;
    }

    public String getManufacturerName() {
        return manufacturerName;
    }

    public void setManufacturerName(String manufacturerName) {
        this.manufacturerName = manufacturerName;
    }

    public String getManufacturerPhoneNo() {
        return manufacturerPhoneNo;
    }

    public void setManufacturerPhoneNo(String manufacturerPhoneNo) {
        this.manufacturerPhoneNo = manufacturerPhoneNo;
    }

    public String getManufacturerPostcode() {
        return manufacturerPostcode;
    }

    public void setManufacturerPostcode(String manufacturerPostcode) {
        this.manufacturerPostcode = manufacturerPostcode;
    }

    public String getManufacturerState() {
        return manufacturerState;
    }

    public void setManufacturerState(String manufacturerState) {
        this.manufacturerState = manufacturerState;
    }
}
